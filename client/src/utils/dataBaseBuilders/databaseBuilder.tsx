export const addItemsToDB = async (itemArray: Ingredient[]) => {
  for (const item of itemArray) {
    try {
      await ingredientService.addIngredient(item);
    } catch (error) {
      console.error(error);
    }
  }
  console.log("done adding items to DB");
};

export function removeUppercaseExceptSeason(arr: Ingredient[]): Ingredient[] {
  return arr.map((ingredient) => {
    const lowercaseIngredient: Ingredient = {
      name: ingredient.name.toLowerCase(),
      alias: ingredient.alias.map((alias) => alias.toLowerCase()),
      pairings: ingredient.pairings.map((pairing) => pairing.toLowerCase()),
      season: ingredient.season.map((season) => season), // Preserve season values in uppercase
      groceryStoreLocation: ingredient.groceryStoreLocation,
    };

    return lowercaseIngredient;
  });
}
export const removeAllItemsFromDb = async () => {
  const ingredients = await ingredientService.getAll();
  if (ingredients) {
    for (const item of ingredients) {
      try {
        if (item.id) {
          await ingredientService.deleteIngredient(item.id);
        }
      } catch (error) {
        console.error(error);
      }
    }
    console.log("done deleting items to DB");
  }
};

export const removeAllItemsInArrayFromDb = async (
  ingredients: Ingredient[]
) => {
  const ingredientsInDB = await ingredientService.getAll();
  try {
    for (const item of ingredients) {
      const itemToDelete = ingredientsInDB?.filter(
        (ingredient) =>
          ingredient.name.toLowerCase() === item.name.toLowerCase()
      );

      if (itemToDelete && itemToDelete[0].id) {
        await ingredientService.deleteIngredient(itemToDelete[0].id);
      }
    }
    console.log("Done deleting items from the DB");
  } catch (error) {
    console.error(error);
  }
};

